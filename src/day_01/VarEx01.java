package day_01;

class VarEx01 
{
	public static void main(String[] args)
	{
		
		int num;
		num = 10;
		System.out.println(num);
		
		double dNum = 3.14;
		System.out.println(dNum);
		
		char c1 = 'a';
		System.out.println(c1);
		
		char c2 = '°¡';
		System.out.println(c2);
		
		String str = "Java";
		System.out.println(str);
		
		boolean result = true;
		System.out.println(result);
	}
}
